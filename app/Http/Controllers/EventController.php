    public function changeStatus(Request $request)
    {
        $event = Event::find($request->event_id);
        if ($event->status){
            $event->status = false;
        }else {
            $event->status = true;
        }
        $event->update();

        return redirect('event/')->with('status', 'Evento actualizado!');
    }

